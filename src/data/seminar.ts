export interface Slide {
  type: 'story' | 'reflection' | 'conclusion' | 'scripture-dark' | 'introduction';
}

export interface SeminarIntroductionSlide extends Slide {
  type: 'introduction';
  title: string;
  subtitle: string;
  content: string[];
  image: string;
}

export interface StorySlide extends Slide {
  type: 'story';
  title: string;
  subtitle?: string;
  story: string[];
  image?: string;
  chapter: string;
  quote?: string;
}

export interface ReflectionSlide extends Slide {
  type: 'reflection';
  question: string;
  subtitle?: string;
}

export interface ConclusionSlide extends Slide {
  type: 'conclusion';
  title: string;
  points: string[];
  verse?: {
    text: string;
    reference: string;
  };
}

export interface ScriptureDarkSlide extends Slide {
  type: 'scripture-dark';
  reference: string;
  book: string;
  chapter: number;
  verses: {
    number: number;
    text: string;
  }[];
}

export type SeminarSlide = StorySlide | ReflectionSlide | ConclusionSlide | ScriptureDarkSlide | SeminarIntroductionSlide;

// Секция/вкладка презентации
export interface SeminarSection {
  id: string;
  name: string;
  slides: SeminarSlide[];
}

